//Types of Inheritance :- 1) Single,   2) Multilevel,   3)Multiple,   4)Hybrid,   5)Hierarchical

/*Single Inheritance => In single inheritance, a class is allowed to inherit from only one class. 
i.e. one subclass is inherited by one base class only.*/

//Multilevel Inheritance => In this type of inheritance, a derived class is created from another derived class.

/*Multiple Inheritance => Multiple Inheritance is a feature of C++ where a class can inherit from more than one class. 
i.e one subclass is inherited from more than one base class.*/

/*Hybrid (Virtual) Inheritance => Hybrid Inheritance is implemented by combining more than one type of inheritance. 
For example: Combining Hierarchical inheritance and Multiple Inheritance.*/

/*Hierarchical Inheritance => In this type of inheritance, more than one subclass is inherited from a single base 
class. i.e. more than one derived class is created from a single base class.*/